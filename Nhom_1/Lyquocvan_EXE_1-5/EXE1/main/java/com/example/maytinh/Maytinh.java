package com.example.maytinh;

public class Maytinh {
String chuoi, kq;

    public Maytinh(String chuoi, String kq) {
        this.chuoi = chuoi;
        this.kq = kq;
    }

    public Maytinh() {

    }

    public String getChuoi() {
        return chuoi;
    }

    public String getKq() {
        return kq;
    }

    public void setChuoi(String chuoi) {
        this.chuoi = chuoi;
    }

    public void setKq(String kq) {
        this.kq = kq;
    }
}
